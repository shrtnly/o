function compileCourse() {
  console.log('🚀 Generating Password & Account Security course...');

  const courseId = '9372bb76-c1eb-4c2f-bcde-96400a579880';
  const units = [];

  courseMetadata.forEach((unitMeta, uIdx) => {
    const unitId = uid();
    const expandedChapters = expandChapters(unitMeta.chapters);

    const chapters = expandedChapters.map((ch, chIdx) => {
      const chapterId = uid();
      const learningPoints = ch.lps.map((lp, lpIdx) => {
        const lpId = uid();
        const qId = uid();

        // Build question
        let mcqOptions = [];
        let metadata = null;
        let narrative = null;

        if (lp.qType === 'matching') {
          metadata = {
            pairs: lp.pairs.map(p => ({ left: p.left, right: p.right }))
          };
        } else {
          // Shuffle options
          const shuffledOpts = shuffle(lp.options);
          mcqOptions = shuffledOpts.map((opt, optIdx) => ({
            id: uid(),
            question_id: qId,
            option_text: opt.text,
            is_correct: opt.is_correct,
            order_index: optIdx + 1,
            created_at: now()
          }));

          if (lp.qType === 'storytelling') {
            const isBool = lp.options.length === 2;
            metadata = { answer_type: isBool ? 'boolean' : 'mcq' };
            narrative = JSON.stringify(lp.narrative);
          }
        }

        const question = {
          id: qId,
          learning_point_id: lpId,
          question_text: lp.qText,
          explanation: lp.explanation,
          order_index: 1,
          created_at: now(),
          narrative: narrative,
          question_type: lp.qType,
          metadata: metadata,
          mcq_options: mcqOptions
        };

        const lpTitle = `${ch.title}: ${lp.title}`;
        return {
          id: lpId,
          chapter_id: chapterId,
          title: lpTitle,
          content: lp.content,
          order_index: lpIdx + 1,
          created_at: now(),
          type: lp.qType === 'storytelling' ? 'storytelling' : 'standard',
          mcq_questions: [question]
        };
      });

      return {
        id: chapterId,
        unit_id: unitId,
        title: ch.title,
        order_index: chIdx + 1,
        created_at: now(),
        is_trophy: false,
        type: "lesson",
        is_claimed: false,
        reward_hearts: 0,
        reward_gems: 0,
        estimated_time: "10 মিনিট",
        learning_points: learningPoints
      };
    });

    units.push({
      id: unitId,
      course_id: courseId,
      title: unitMeta.unitTitle,
      order_index: uIdx + 1,
      created_at: now(),
      chapters: chapters
    });
  });

  const courseData = {
    id: courseId,
    title: "পাসওয়ার্ড ও অ্যাকাউন্ট সুরক্ষা",
    students_count: 0,
    rating: 0,
    image_url: "https://images.unsplash.com/photo-1555949963-ff9fe0c870eb?auto=format&fit=crop&w=800&q=80",
    is_featured: true,
    created_at: now(),
    description: "পাসওয়ার্ডের মৌলিক ধারণা থেকে শুরু করে টু-ফ্যাক্টর অথেনটিকেশন এবং পাসওয়ার্ড ম্যানেজারের বিস্তারিত গাইড।",
    category: "Digital Literacy & Security",
    title_en: "Password & Account Security",
    status: "published",
    description_en: null,
    units: units
  };

  const finalJson = JSON.stringify(courseData, null, 2);

  const dbDir = path.join(process.cwd(), 'database', 'courses');
  if (!fs.existsSync(dbDir)) {
    fs.mkdirSync(dbDir, { recursive: true });
  }

  const scratchDir = path.join(process.cwd(), 'scratch');
  if (!fs.existsSync(scratchDir)) {
    fs.mkdirSync(scratchDir, { recursive: true });
  }

  const dbPath = path.join(dbDir, 'password_and_account_security.json');
  const scratchPath = path.join(scratchDir, 'password_and_account_security_backup.json');

  fs.writeFileSync(dbPath, finalJson, 'utf-8');
  fs.writeFileSync(scratchPath, finalJson, 'utf-8');

  console.log('✓ Course compiled successfully!');
  console.log(`  - ${dbPath}`);
  console.log(`  - ${scratchPath}`);

  // Calculate final statistics
  const totalChapters = units.reduce((s, u) => s + u.chapters.length, 0);
  const totalLPs = units.reduce((s, u) => s + u.chapters.reduce((s2, c) => s2 + c.learning_points.length, 0), 0);
  const totalQs = units.reduce((s, u) => s + u.chapters.reduce((s2, c) => s2 + c.learning_points.reduce((s3, lp) => s3 + lp.mcq_questions.length, 0), 0), 0);

  console.log(`📊 Final Stats:`);
  console.log(`   Units: ${units.length}`);
  console.log(`   Chapters: ${totalChapters}`);
  console.log(`   Learning Points: ${totalLPs}`);
  console.log(`   Questions: ${totalQs}`);
}

compileCourse();
