function compileCourse() {
  console.log('🚀 Compiling Productivity and Time Management course...');

  const dbJsonPath = path.join(process.cwd(), 'scratch', 'db_productivity_course.json');
  if (!fs.existsSync(dbJsonPath)) {
    console.error(`Error: Hollow database dump not found at ${dbJsonPath}`);
    process.exit(1);
  }

  // Load the hollow course structure to preserve IDs
  const courseData = JSON.parse(fs.readFileSync(dbJsonPath, 'utf-8'));
  const courseId = courseData.id;

  courseData.units.forEach((unit, uIdx) => {
    const unitMeta = courseMetadata[uIdx];
    if (!unitMeta) {
      console.error(`Error: No metadata found for unit index ${uIdx}`);
      process.exit(1);
    }

    console.log(`Processing Unit ${uIdx + 1}: ${unit.title}`);

    unit.chapters.forEach((chapter, chIdx) => {
      const chMeta = unitMeta.chapters[chIdx];
      if (!chMeta) {
        console.error(`Error: No metadata found for unit ${uIdx + 1}, chapter ${chIdx + 1}`);
        process.exit(1);
      }

      // Check title match (with allowance for slight DB title deviations)
      const expectedTitle = chMeta.titleDb || chMeta.title;
      if (chapter.title !== expectedTitle) {
        console.warn(`⚠️ Warning: Title mismatch at unit ${uIdx + 1}, chapter ${chIdx + 1}. DB title: "${chapter.title}", Metadata expected: "${expectedTitle}"`);
      }

      // Expand the metadata chapter into 8 LPs
      const expanded = expandChapters([chMeta])[0];

      // Build learning points with proper IDs and relations
      const learningPoints = expanded.lps.map((lp, lpIdx) => {
        const lpId = uid();
        const qId = uid();

        let mcqOptions = [];
        let metadata = null;
        let narrative = null;

        if (lp.qType === 'matching') {
          metadata = {
            pairs: lp.pairs.map(p => ({ left: p.left, right: p.right }))
          };
        } else {
          // Shuffle options
          const shuffledOpts = shuffle(lp.options);
          mcqOptions = shuffledOpts.map((opt, optIdx) => ({
            id: uid(),
            question_id: qId,
            option_text: opt.text,
            is_correct: opt.is_correct,
            order_index: optIdx + 1,
            created_at: now()
          }));

          if (lp.qType === 'storytelling') {
            const isBool = lp.options.length === 2;
            metadata = { answer_type: isBool ? 'boolean' : 'mcq' };
            narrative = JSON.stringify(lp.narrative);
          }
        }

        const question = {
          id: qId,
          learning_point_id: lpId,
          question_text: lp.qText,
          explanation: lp.explanation,
          order_index: 1,
          created_at: now(),
          narrative: narrative,
          question_type: lp.qType,
          metadata: metadata,
          mcq_options: mcqOptions
        };

        const lpTitle = `${chapter.title}: ${lp.title}`;
        return {
          id: lpId,
          chapter_id: chapter.id,
          title: lpTitle,
          content: lp.content,
          order_index: lpIdx + 1,
          created_at: now(),
          type: lp.qType === 'storytelling' ? 'storytelling' : 'standard',
          mcq_questions: [question]
        };
      });

      // Update the chapter object in place, keeping existing fields
      chapter.learning_points = learningPoints;
      chapter.reward_hearts = chapter.reward_hearts || 5;
      chapter.reward_gems = chapter.reward_gems || 20;
      chapter.estimated_time = chapter.estimated_time || "10 মিনিট";
    });
  });

  const finalJson = JSON.stringify(courseData, null, 2);

  const dbDir = path.join(process.cwd(), 'database', 'courses');
  if (!fs.existsSync(dbDir)) {
    fs.mkdirSync(dbDir, { recursive: true });
  }

  const dbPath = path.join(dbDir, 'productivity_and_time_management.json');
  const backupPath = path.join(process.cwd(), 'scratch', 'productivity_and_time_management_backup.json');

  fs.writeFileSync(dbPath, finalJson, 'utf-8');
  fs.writeFileSync(backupPath, finalJson, 'utf-8');

  console.log('✓ Course compiled successfully!');
  console.log(`  - Output: ${dbPath}`);
  console.log(`  - Backup: ${backupPath}`);

  // Calculate final statistics
  const totalChapters = courseData.units.reduce((s, u) => s + u.chapters.length, 0);
  const totalLPs = courseData.units.reduce((s, u) => s + u.chapters.reduce((s2, c) => s2 + c.learning_points.length, 0), 0);
  const totalQs = courseData.units.reduce((s, u) => s + u.chapters.reduce((s2, c) => s2 + c.learning_points.reduce((s3, lp) => s3 + lp.mcq_questions.length, 0), 0), 0);

  console.log(`📊 Final Stats:`);
  console.log(`   Units: ${courseData.units.length}`);
  console.log(`   Chapters: ${totalChapters}`);
  console.log(`   Learning Points: ${totalLPs}`);
  console.log(`   Questions: ${totalQs}`);
}

compileCourse();
